def calcular_resultado(n1, n2, n3):
    resultado_calculo = n1 * n2 + n3
    return resultado_calculo

def mostrar_resultado():
    Numero1 = 5
    Numero2 = 3
    Numero3 = 7
    resultado_final = calcular_resultado(Numero1, Numero2, Numero3)
    print("El resultado es:", resultado_final)

mostrar_resultado()

