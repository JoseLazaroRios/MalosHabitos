def calcular_resultado(a, b, c):
    resultado_calculo = a * b + c
    return resultado_calculo

def mostrar_resultado():
    valor_x = 5
    valor_y = 3
    valor_z = 7
    resultado_final = calcular_resultado(valor_x, valor_y, valor_z)
    print("El resultado es:", resultado_final)

mostrar_resultado()

